void ILI9225_DrawPixel(uint16_t x, uint16_t y, color_t color) {
	if((x >= _maxX) || (y >= _maxY)) return;

	//ILI9225_SetWindow(x,y,x+1,y+1);
	ILI9225_OrientCoordinates(&x, &y);
	//directly write SPI
	/*uint8_t data[] = { color >> 8, color & 0xff};
	ILI9225_LCD_Data(data, 2);*/
	int index = x + y*ILI9225_LCD_WIDTH;
	//printf("x,y,i = (%d,%d,%d)\n", x,y, index);
	if(index >= ili9225_framebuf_elements) return;
	ili9225_framebuf[index] = color;
}

void ILI9225_DrawXBitmap(int16_t x, int16_t y, const uint8_t *bitmap, int16_t w, int16_t h, uint16_t color) {
	int16_t i, j, byteWidth = (w + 7) / 8;
	uint8_t byte;
	for (j = 0; j < h; j++) {
		for (i = 0; i < w; i++ ) {
			if (i & 7) byte >>= 1;
			else      byte   = pgm_read_byte(bitmap + j * byteWidth + i / 8);
			if (byte & 0x01) ILI9225_DrawPixel(x + i, y + j, color);
		}
	}
}

void ILI9225_DrawBitmap(int16_t x, int16_t y, const uint8_t *bitmap, int16_t w, int16_t h, uint16_t color) {
	int16_t i, j, byteWidth = (w + 7) / 8;
	uint8_t byte;

	for(j = 0; j < h; j++) {
		for(i = 0; i < w; i++) {
			if (i & 7) byte <<= 1;
			else      byte   = bitmap[j * byteWidth + i / 8];
			if (byte & 0x80) ILI9225_DrawPixel(x + i, y + j, color);
		}
	}
}

