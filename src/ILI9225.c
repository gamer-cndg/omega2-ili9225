void ILI9225_SetOrientation(uint8_t orientation) {
	_orientation = orientation % 4;

	switch (_orientation) {
	case 0:
		_maxX = ILI9225_LCD_WIDTH;
		_maxY = ILI9225_LCD_HEIGHT;
		break;
	case 1:
		_maxX = ILI9225_LCD_HEIGHT;
		_maxY = ILI9225_LCD_WIDTH;
		break;
	case 2:
		_maxX = ILI9225_LCD_WIDTH;
		_maxY = ILI9225_LCD_HEIGHT;
		break;
	case 3:
		_maxX = ILI9225_LCD_HEIGHT;
		_maxY = ILI9225_LCD_WIDTH;
		break;
	}
}

static void ILI9225_OrientCoordinates(uint16_t *x1, uint16_t *y1) {

	switch (_orientation) {
	case 0:  // ok
		break;
	case 1: // ok
		*y1 = (uint16_t)(_maxY - *y1 - 1);
		_swap(*x1, *y1);
		break;
	case 2: // ok
		*x1 = _maxX - *x1 - 1;
		*y1 = _maxY - *y1 - 1;
		break;
	case 3: // ok
		*x1 = _maxX - *x1 - 1;
		_swap(*x1, *y1);
		break;
	}
}

