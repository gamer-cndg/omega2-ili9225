bool ILI9225_Init(ili9225_initparams_t* initParams){
	printf("[-] Init function called.\n");
	//activate debug logging
	//onionSetVerbosity(ONION_VERBOSITY_EXTRA_VERBOSE);

	int err = 0;

	if(!initParams)
		return false;

	//check if all required GPIOs are set
	if(initParams->cs < 0 || initParams->mosi < 0 || initParams->sclk < 0 || initParams->rs < 0) {
		printf("[-] Required GPIO pin not set.\n");
		return false;
	}

	//use default values at first
	spiParamInit(&params);
	params.mosiGpio = initParams->mosi;
	params.sckGpio = initParams->sclk;
	params.csGpio = initParams->cs;
	params.speedInHz = initParams->speedInHz;
	params.modeBits = SPI_MODE_0; //only SPIMODE_0, no dual TX or RX as "default"..
	params.busNum = 1; //this comes from my local machine. no idea if it works elsewhere.. (ls /dev/spi*)
	params.deviceId = 32766;
	params.delayInUs = 10;

	//is our device already mapped?
	err = spiCheckDevice(params.busNum, params.deviceId, ONION_SEVERITY_DEBUG);
	if(err == EXIT_FAILURE) {
		printf("[-] spiCheckDevice() failed.\n");
		return false;
	}

	//register ourselves
	err = spiRegisterDevice(&params);
	if(err == EXIT_FAILURE)  {
		printf("spiRegisterDevice() failed.\n");
		return false;
	}
	printf("[+] SPI register device okay.\n");

	err = spiSetupDevice(&params);
	if(err == EXIT_FAILURE)  {
		printf("spiSetupDevice() failed.\n");
		return false;
	}

	//set GPIO for DC (data/command selection, on the board called "RS")
	savedRS = initParams->rs;
	ILI9225_InitOutputGPIO(initParams->rs);
	//reset line (optional)
	if(initParams->rst > 0) {
		savedRst = initParams->rst;
		ILI9225_InitOutputGPIO(initParams->rst);
	}
	//LED line (turn display on/off, optional)
	if(initParams->led > 0) {
		savedLED = initParams->led;
		ILI9225_InitOutputGPIO(initParams->led);
	}

	//pulse reset pin
	if(initParams->rst > 0) {
		ILI9225_SetOutputGPIO(savedRst, 1);
		usleep(100 * 1000);
		ILI9225_SetOutputGPIO(savedRst, 0);
		usleep(100 * 1000);
		ILI9225_SetOutputGPIO(savedRst, 1);
		usleep(100 * 1000);
	}

	//Do initialization sequence
	ILI9225_DoInitSequence();

	return true;
}

void ILI9225_DeInit() {
	//free GPIOs
	ILI9225_FreeGPIOs();
	//free SPI bus
}

static bool ILI9225_InitOutputGPIO(int gpio) {
	if(gpio < 0) return false;
	unsigned int gpio_u = (unsigned int) gpio;
	//use ugpio.
	//is the gpio already requested by somebody else?
	int err;
	if ((err = gpio_is_requested(gpio_u)) < 0) {
		printf("[-] GPIO %d already requested.\n", gpio);
		return false;
	}
	//export GPIO if necessary
	if (!err) {
		printf("Exporting GPIO %d\n", gpio);
		if ((err = gpio_request(gpio_u, NULL)) < 0) {
			printf("[-] Requesting GPIO %d failed.\n", gpio);
			return false;
		}
	}
	if((err = gpio_direction_output(gpio_u, 1)) < 0) {
		printf("[-] Setting GPIO %d to output mode failed.\n", gpio);
		return false;
	}

	return true;
}

//Just a wrapper for gpio_set_value
static void ILI9225_SetOutputGPIO(int gpio, int value) {
	if(gpio < 0) return;
	gpio_set_value((unsigned int)gpio, value);
}

static void ILI9225_FreeGPIOs() {
	if(savedRS != -1) {
		gpio_free(savedRS);
		savedRS = -1; //prevent double freeing
	}
	if(savedRst != -1) {
		gpio_free(savedRst);
		savedRst = -1;
	}
	if(savedLED != -1) {
		gpio_free(savedLED);
		savedLED = -1;
	}
}

static void ILI9225_LCD_Cmd(const uint8_t cmd) {
	//set d/c must be 0
	ILI9225_SetOutputGPIO(savedRS, 0);
	//transmit SPI data
	uint8_t cmdCopy = cmd;
	uint8_t dummyRXBuf = 0;
	int err = spiTransfer(&params, &cmdCopy, &dummyRXBuf, 1);
	if(err == EXIT_FAILURE)
		printf("[-] SPI transfer failed!\n");
}

#define ILI9225_DUMMY_RX_BUF_LEN (ILI9225_LCD_WIDTH * ILI9225_LCD_HEIGHT * sizeof(uint16_t))
static uint8_t dummyRXBuf[ILI9225_DUMMY_RX_BUF_LEN];

static void ILI9225_LCD_Data(const uint8_t *data, int len) {
	if(len > ILI9225_DUMMY_RX_BUF_LEN) { printf("[-] Not drawing overlong data.\n"); }
	//set d/c must be 1
	ILI9225_SetOutputGPIO(savedRS, 1);
	//transmit SPI data
	int err = spiTransfer(&params, (uint8_t*) data, dummyRXBuf, len);
	if(err == EXIT_FAILURE)
		printf("[-] SPI transfer failed!\n");
}

static void ILI9225_WriteRegister(uint16_t cmd, uint16_t data) {
	ILI9225_LCD_Cmd((uint8_t)(cmd >> 8));
	ILI9225_LCD_Cmd((uint8_t)(cmd & 0xff));
	uint8_t _data[2] = { (uint8_t)(data >> 8), (uint8_t)(data & 0xff) };
	ILI9225_LCD_Data(_data, sizeof(_data));
}


static void ILI9225_DoInitSequence() {
	int cmd = 0;
    while (ili9225_init_cmds[cmd].databytes!=0xff) {
        ILI9225_LCD_Cmd(ili9225_init_cmds[cmd].cmd);
        ILI9225_LCD_Data(ili9225_init_cmds[cmd].data, ili9225_init_cmds[cmd].databytes&0x1F);
        if (ili9225_init_cmds[cmd].databytes&0x80) {
        	usleep(100 * 1000);
        }
        cmd++;
    }
}

static void ILI9225_SetWindow(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1) {
	ILI9225_WriteRegister(ILI9225_HORIZONTAL_WINDOW_ADDR1,x1);
	ILI9225_WriteRegister(ILI9225_HORIZONTAL_WINDOW_ADDR2,x0);

	ILI9225_WriteRegister(ILI9225_VERTICAL_WINDOW_ADDR1,y1);
	ILI9225_WriteRegister(ILI9225_VERTICAL_WINDOW_ADDR2,y0);

	ILI9225_WriteRegister(ILI9225_RAM_ADDR_SET1,x0);
	ILI9225_WriteRegister(ILI9225_RAM_ADDR_SET2,y0);

	ILI9225_LCD_Cmd(0x00);
	ILI9225_LCD_Cmd(0x22);
}

//framebuffer is assumed to have the correct length.
void ILI9225_CopyExternalFrameBuffer(uint8_t* framebuf, size_t framebufLen) {
	ILI9225_SetWindow(0, 0, ILI9225_LCD_WIDTH - 1, ILI9225_LCD_HEIGHT -1);
	// d/c line must be 1
	ILI9225_SetOutputGPIO(savedRS, 1);
	//write our entire framebuffer
	//the SPI driver doesn't seem to like it when we give it our entire ~72 kByte buffer..
	//we have to write it in chunks...
	//max length seems to be 16 bytes... / 8 color values
	//transfer blockwise
	const int block_size = 4096;
	int remainingBytes = framebufLen;

	for(int i=0; i < (int)(framebufLen); i += block_size) {
			uint8_t currTX[block_size];
			uint8_t* currFB = framebuf + i;
			//Copy framebuffer *and* correct endianess in one go
			int transfersize = remainingBytes < block_size ? remainingBytes : block_size;

			for(int j=0; j < transfersize; j+=2) {
				currTX[j] = currFB[j+1];
				currTX[j+1] = currFB[j];
			}
			spiTransfer(&params, currTX, dummyRXBuf, transfersize);
			remainingBytes -= transfersize;

			//write each color byte one-by-one
			/*int err = spiTransfer(&params, currTX, dummyRXBuf, block_size);
				if(err == EXIT_FAILURE)
					printf("[-] SPI transfer failed!\n");*/
	}
	//onionSetVerbosity(ONION_VERBOSITY_EXTRA_VERBOSE);
	//lower line again
	ILI9225_SetOutputGPIO(savedRS, 0);
}

